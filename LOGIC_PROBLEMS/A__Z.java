package LOGIC_PROBLEMS;

public class A__Z {

	public static void main(String[] args) {
		// Program to print characters from A to Z

		// Loop runs from character 'A' to 'Z'
		for (char ch ='A'; ch<='Z';ch++) {
			System.out.print(ch+" ");
			
		}
	}

}

