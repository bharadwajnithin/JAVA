package BASIC;

public class PrintNumbers {
	public static void main(String[] args) {
		{
	int n = 10;
	
	// 1 to N
	for (int i=1;i<=n;i++) 
	{
		System.out.println("number is "+i);
		System.out.println("         ");
		
		
	}
	
	/// n to 1
	
	{
		
		for (int i=10;i>=n;i--) {
			
			System.out.println("number is "+i);
			System.out.println("         ");
		}
	}
	
	
	// sum of natural numbers 
	
	
	
	{
		int m=0;
		for (int i=1;i<=n;i++) 
		{
		m+=i;			
		}
		System.out.println("the sum of 1st n natural number is :"+m);
		
		
	}
	
	
	}}}


